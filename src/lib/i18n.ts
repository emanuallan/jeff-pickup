export type Lang = "en" | "es";

const LANG_KEY = "jeffpickup.lang";

export function loadLang(): Lang {
	try {
		const v = localStorage.getItem(LANG_KEY);
		return v === "es" ? "es" : "en";
	} catch {
		return "en";
	}
}

export function saveLang(lang: Lang) {
	try {
		localStorage.setItem(LANG_KEY, lang);
	} catch {
		// ignore
	}
}

